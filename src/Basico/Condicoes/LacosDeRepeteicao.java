package Basico.Condicoes;

public class LacosDeRepeteicao {
    public static void main(String[] args) {
        // Usando o laço for para treinar Jutsus 5 vezes
        for (int i = 1; i <= 5; i++)
        { System.out.println("Treinamento de Jutsu número: " + i); }
        // Usando o laço do-while para simular o treinamento de controle de chakra int chakra = 100; do { System.out.println("Controlando o chakra, energia restante: " + chakra); chakra -= 10; // Reduzindo o chakra em 10 a cada iteração } while (chakra > 0);

        System.out.println("Treinamento concluído. O chakra foi completamente utilizado.");
    }
}
